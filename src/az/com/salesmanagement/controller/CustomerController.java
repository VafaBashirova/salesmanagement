    private void addComboData(){
        DictionaryDao dao=new DictionaryDao();
        try {
            List<String> dics=dao.dictonaryContents(DictionaryEnum.CUSTOMER_TYPE.getKey());
            for(String e : dics){
                customerType.addItem(e);
                searchCustomerByType.addItem(e);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    private void addContents(){
        CustomersDao dao=new CustomersDao();
        try {
            List<Customers> customersList=dao.customersTableContents();
            DefaultTableModel customersTb=(DefaultTableModel)customersTable.getModel();
            Object columns[]=new Object[4];
            for(Customers e:customersList){
                columns[0]=e.getId();
                columns[1]=e.getName();
                columns[2]=e.getVoen();
                columns[3]=e.getTypeLabel();
                Integer existence=e.getExistence();
                if(existence==1) customersTb.addRow(columns);
            }
        } catch (SQLException ex) {
            Logger.getLogger(CustomerController.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }
    private void addSelectedContents(String name,String voen,Integer type){
        CustomersDao dao=new CustomersDao();
        try {
            List<Customers> customersList=dao.selectedCustomers(name, voen, type);
            DefaultTableModel customersTb=(DefaultTableModel)customersTable.getModel();
            Object columns[]=new Object[4];
            for(Customers e:customersList){
                columns[0]=e.getId();
                columns[1]=e.getName();
                columns[2]=e.getVoen();
                columns[3]=e.getTypeLabel();
                Integer existence=e.getExistence();
                if(existence==1) customersTb.addRow(columns);
            }
        } catch (SQLException ex) {
            Logger.getLogger(CustomerController.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }
