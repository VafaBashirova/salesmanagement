    private void customersInsertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_customersInsertButtonActionPerformed
        CustomersDao dao=new CustomersDao();
        try {
            Customers customer=new Customers();
            customer.setName(name());
            customer.setVoen(voen());
            customer.setType(type());
            dao.addCustomer(customer);
            DefaultTableModel model=(DefaultTableModel)customersTable.getModel();
            model.setRowCount(0);
            addContents();
        } catch (SQLException ex) {
            Logger.getLogger(CustomerController.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_customersInsertButtonActionPerformed

